package tools;

import java.awt.geom.Rectangle2D;

/** 
 * Makes a rectangle and adds it to it's drawing panel.
 * @author Erica Putsche
 * @version 05/15/2013
 */
public class Rectangle extends Abstract2DTool {
  /**
   * Builds a rectangle shape.
   * @param the_x The x coordinate of the upper left corner of this rectangle.
   * @param the_y The y coordinate of the upper left corner of this rectangle.
   * @param the_width The width of this rectangle.
   * @param the_height The height of this rectangle.
   * @return The rectangle.
   */
  protected Rectangle2D.Double buildShape(final double the_x, final double the_y, 
                           final double the_width, final double the_height) {
    
    return new Rectangle2D.Double(the_x, the_y, the_width, the_height);
  }
}
